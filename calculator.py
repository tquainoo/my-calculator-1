#import math module to use the sqrt function provided below
import math

def add(a, b):
    return a + b

def subtract(a, b):
    return a - b

def multiply(a, b):
    return a * b

def divide(a, b):
    return a / b

def sqrt(x):
    #find and Return the square root of the variable x
    return math.sqrt(x)
